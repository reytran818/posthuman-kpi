async function seed() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;
  if (!token) {
    console.error("❌ No BLOB_READ_WRITE_TOKEN found in .env.local");
    process.exit(1);
  }

  const { url } = await put("posthuman-founders.json", JSON.stringify(founders), {
    access: "public",
    addRandomSuffix: false,
    allowOverwrite: true,
    token,
  });
  
  console.log("✅ Founder data seeded successfully");
  console.log(`   ${founders.length} founders with ${founders.reduce((s, f) => s + f.kpis.length, 0)} total KPIs`);
  console.log(`   Blob URL: ${url}`);
}

seed();
